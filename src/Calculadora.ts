import state from '../src/State';
export default class calculadora {

    static sumar (a:number, b:number){

        return a+b;
    }

    static restar (a:number, b:number){

        return a-b;
    }
    
    static multiplicar (a:number, b:number){

        return a*b;
    }
    
    static dividir (a:number, b:number){

        return a/b;
    }

}